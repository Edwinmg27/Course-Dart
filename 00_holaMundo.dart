void main() {
  // main() es el punto de entrada obligatorio en Dart.
  print('Hola, Dart'); // print() envía datos a la consola.

  String nameUser = "Edwin";
  int ageUser = 30;

  print("Hola $nameUser, tu edad es de $ageUser años de edad");
}
